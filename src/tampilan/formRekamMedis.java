     protected void dataTable(){
        Object[] baris = {"No Pendaftaran","ID Dokter","Nama Dokter","No Identitas Pasien","Nama Pasien","Golongan Darah",
        "Tinggi Badan","Berat Badan","Area Diagnosa Dokter","Tanggal Pemeriksaan"};
        
        tabmode = new DefaultTableModel(null, baris);
        tabel_riwayat_pasien.setModel(tabmode);
        String sql = "Select * from dokter";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("no_pendaftaran");
                String b = hasil.getString("id_dokter");
                String c = hasil.getString("nama_dokter");
                String d = hasil.getString("no_identitas_pasien");
                String e = hasil.getString("nama_pasien");
                String f = hasil.getString("golongan_darah");
                String g = hasil.getString("tinggi_badan");
                String h = hasil.getString("berat_badan");
                String i = hasil.getString("diagnosa_dokter");
                String j = hasil.getString("tgl_pemeriksaan");
                
                String [] data = {a,b,c,d,e,f,g,h,i,j};
                tabmode.addRow(data);
            }
        }catch (Exception e){
        }
    }
