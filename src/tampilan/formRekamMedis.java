    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        String sql = "insert into dokter values (?,?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1,jTextField_no_pendaftaran.getText());
            stat.setString(2,jTextField_id_dokter.getText());
            stat.setString(3,jTextField_nama_dokter.getText());
            stat.setString(4,jTextField_no_identitas_pasien.getText());
            stat.setString(5,jTextField_nama_pasien.getText());
            stat.setString(6,jTextField_golongan_darah.getText());
            stat.setString(7,jTextField_tinggi_badan.getText());
            stat.setString(8,jTextField_berat_badan.getText());
            stat.setString(9,jTextArea_diagnosa_dokter.getText());
            stat.setString(10,jTextField_tanggal_pemeriksaan.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil Disimpan");
            kosong();
            jTextField_no_pendaftaran.requestFocus();
            dataTable();
          } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan" + e);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

